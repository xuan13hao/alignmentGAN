r1 = "GAAAATCTCATCTTGAATTGTAGCTCCCATAATCCCCACATGTTGTGGGAGGGACCCAGTGGGAGATAATTGAATCATGGTGGTGGGTTTTCCCCATGCTG"
dis1 = hamming_distance(t1,r1) 
print(dis1) # 65 Similarity = 35.6%

